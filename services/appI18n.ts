export function translate(
  language: AppLanguage,
  key: string,
  params?: Record<string, string | number | undefined>
) {
  const fallback = TRANSLATIONS.es[key] ?? key;
  const value = TRANSLATIONS[language][key] ?? fallback;

  if (!params) {
    return value;
  }

  return Object.entries(params).reduce((current, [name, replacement]) => {
    return current.replaceAll(`{${name}}`, String(replacement ?? ''));
  }, value);
}
